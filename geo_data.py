_TOWN_RE = re.compile(
    r"(?<![a-z])(" +
    "|".join(re.escape(n) for n in sorted(MA_TOWNS, key=len, reverse=True)) +
    r")(?![a-z])")


def miles_between(lat1, lng1, lat2, lng2):
    """Great-circle distance in miles (haversine)."""
    rad = math.radians
    dlat, dlng = rad(lat2 - lat1), rad(lng2 - lng1)
    a = (math.sin(dlat / 2) ** 2 +
         math.cos(rad(lat1)) * math.cos(rad(lat2)) * math.sin(dlng / 2) ** 2)
    return 2 * 3958.8 * math.asin(math.sqrt(a))


def miles_from_home(lat, lng):
    return miles_between(HOME["lat"], HOME["lng"], lat, lng)


def town_lookup(*texts):
    """Find a Massachusetts town name in the given text fields (longest name
    wins). Returns {lat, lng, matched, remote} like resolve(), or None."""
    blob = " ".join(t for t in texts if t).lower()
    if not blob:
        return None
    m = _TOWN_RE.search(blob)
    if not m:
        return None
    lat, lng = MA_TOWNS[m.group(1)]
    return {"lat": lat, "lng": lng, "matched": m.group(1),
            "remote": "remote" in blob}


def town_distance(town_name):
    """Distance in miles from home to a town's center, or None if the name
    isn't a known Massachusetts municipality."""
    hit = town_lookup(town_name)
    return round(miles_from_home(hit["lat"], hit["lng"]), 1) if hit else None
